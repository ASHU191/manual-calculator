void main() {
  int num1 = 88;
  String operator = "+";
  int num2 = 3;

  if (operator == "+") {
    print(num1 + num2);
  } else if (operator == "-") {
    print(num1 - num2);
  } else if (operator == "/") {
    print(num1 / num2);
  } else if (operator == "*") {
    print(num1 * num2);
  }
}
